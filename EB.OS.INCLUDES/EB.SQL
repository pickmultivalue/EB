  IF INDEX(DSPLY,'No errors.',1) THEN
    CRT DSPLY<2>
  END ELSE
    SCR.UD=1
    CONVERT CR TO '' IN DSPLY
    NBR.DSPLY=DCOUNT(DSPLY,AM)
    FOR I=1 TO NBR.DSPLY UNTIL SYSTEM(14)
      TXT=DSPLY<I>
      IF INDEX(TXT,'ignored',1) THEN
        DEL DSPLY<I>
        I-=1
        NBR.DSPLY-=1
      END ELSE
        CRT TXT
      END
    NEXT I
    CRT
    TXT="re-edit"
    DSPCNT = DCOUNT(DSPLY[1,INDEX(DSPLY,'LINE/COL',1)],AM)
    ROW=TRIM(DSPLY<DSPCNT+2>)
    ROW=FIELD(OCONV(ROW,'MCP'),'.',1)
    INDROW=FIELD(ROW,'/',1)
    COL=ROW[COL2()+1,MAX]+OFFSET+4
    INDROW-=11
    IF COL<5 THEN COL=5
    ROW=11
    IF INDROW<1 THEN ROW=ROW+INDROW-1; INDROW=1
    CRT MSG.CLR:
    SCR.UD=1
  END
